def random_nickname() -> str:
    """随机生成一个「{形容词}的{名词}」文艺昵称，如「深夜的造物者」。"""
    return f"{random.choice(_ADJECTIVES)}的{random.choice(_NOUNS)}"


def random_avatar_seed() -> str:
    """随机生成头像种子。前端用它确定性地推导出渐变色 + emoji。

    用 secrets 而非 random：种子虽不敏感，但用密码学随机能保证更均匀、不可预测，
    避免同一时刻批量注册撞到相近种子。
    """
    return secrets.token_hex(6)
